#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*src;

	src = (char *)s2;
	while (*s1)
		s1++;
	while (n-- > 0 && *src)
		*s1++ = *src++;
	*s1 = 0;
	return (s1);
}
